package com.ivanabandeira.entradadados;

//Biblioteca de java para entrada de dados

public class EntradaDados {

		 public static void main(String args[]) {
			   int aux = 10;

		        metodoDois(aux);

		        System.out.println(aux);

		        metodoUm(aux);

		        System.out.println(aux);

		    }

		    public static void metodoUm(int val) {

		        val = 20;

		        System.out.println(val);

		    }

		    public static void metodoDois(int val) {

		        val++;

		        System.out.println(val);

		    }

			        
          }
		

	



